<h3 class="text-xl font-extrabold text-gray-800  sm:text-2xl md:-mb-4 -mb-1">
    Was sind Google Fonts?
</h3>
<p>
    Google Fonts (früher Google Web Fonts) ist ein interaktives Verzeichnis mit mehr als 800 Schriftarten,
    die die Google LLC zur freien Verwendung bereitstellt.
</p>
<p>
    Viele dieser Schriftarten sind unter der SIL Open Font License veröffentlicht,
    während andere unter der Apache-Lizenz veröffentlicht wurden. Beides sind freie Software-Lizenzen.
    Somit können wir sie frei verwenden, ohne dafür Lizenzgebühren zu zahlen.
</p>

<h3 class="text-xl font-extrabold text-gray-800  sm:text-2xl md:-mb-4 -mb-1">
    Warum verwenden wir Google Fonts auf unserer Webseite?
</h3>
<p>
    Mit Google Fonts können wir auf der eigenen Webseite Schriften nutzen,
    und müssen sie nicht auf unserem eigenen Server hochladen.
    Google Fonts ist ein wichtiger Baustein, um die Qualität unserer Webseite hoch zu halten.
    Alle Google-Schriften sind automatisch für das Web optimiert und dies spart Datenvolumen und
    ist speziell für die Verwendung bei mobilen Endgeräten ein großer Vorteil.
    Wenn Sie unsere Seite besuchen, sorgt die niedrige Dateigröße für eine schnelle Ladezeit.
    Des Weiteren sind Google Fonts sogenannte sichere Web Fonts.
    Unterschiedliche Bildsynthese-Systeme (Rendering) in verschiedenen Browsern,
    Betriebssystemen und mobilen Endgeräten können zu Fehlern führen.
    Solche Fehler können teilweise Texte bzw. ganze Webseiten optisch verzerren.
    Dank des schnellen Content Delivery Network (CDN) gibt es mit Google Fonts
    keine plattformübergreifenden Probleme. Google Fonts unterstützt alle gängigen
    Browser ( Google Chrome, Mozilla Firefox, Apple Safari, Opera) und funktioniert
    zuverlässig auf den meisten modernen mobilen Betriebssystemen,
    einschließlich Android 2.2+ und iOS 4.2+ (iPhone, iPad, iPod).
</p>
<p>
    Wir verwenden die Google Fonts also, damit wir unser gesamtes Online-Service so schön und einheitlich
    wie möglich darstellen können. Nach dem Art. 6 Abs. 1 f lit. F DSGVO stellt das bereits
    ein „berechtigtes Interesse“ an der Verarbeitung von personenbezogenen Daten dar.
    Unter „berechtigtem Interesse“ versteht man in diesem Fall sowohl rechtliche als auch
    wirtschaftliche oder ideelle Interessen, die vom Rechtssystem anerkannt werden.
</p>

<h3 class="text-xl font-extrabold text-gray-800  sm:text-2xl md:-mb-4 -mb-1">
    Welche Daten werden von Google gespeichert?
</h3>
<p>
    Wenn Sie unsere Webseite besuchen, werden die Schriften über einen Google-Server nachgeladen. Durch diesen externen Aufruf werden Daten an die Google-Server übermittelt. So erkennt Google auch, dass Sie bzw. Ihre IP-Adresse unsere Webseite besucht. Die Google Fonts API wurde entwickelt, um die Erfassung, Speicherung und Verwendung von Endnutzerdaten auf das zu reduzieren, was für eine effiziente Bereitstellung von Schriften nötig ist. API steht übrigens für „Application Programming Interface“ und dient unter anderem als Datenübermittler im Softwarebereich.
</p>
<p>
    Google Fonts speichert CSS- und Font-Anfragen sicher bei Google und ist somit geschützt. Durch die gesammelten Nutzungszahlen kann Google die Beliebtheit der Schriften feststellen. Die Ergebnisse veröffentlicht Google auf internen Analyseseiten, wie beispielsweise Google Analytics. Zudem verwendet Google auch Daten des eigenen Web-Crawlers, um festzustellen, welche Webseiten Google-Schriften verwenden. Diese Daten werden in der BigQuery-Datenbank von Google Fonts veröffentlicht. BigQuery ist ein Webservice von Google für Unternehmen, die große Datenmengen bewegen und analysieren wollen.
</p>
<p>
    Zu bedenken gilt allerdings noch, dass durch jede Google Font Anfrage auch Informationen wie IP-Adresse, Spracheinstellungen, Bildschirmauflösung des Browsers, Version des Browsers und Name des Browsers automatisch an die Google-Server übertragen werden. Ob diese Daten auch gespeichert werden, ist nicht klar feststellbar bzw. wird von Google nicht eindeutig kommuniziert.
</p>

<h3 class="text-xl font-extrabold text-gray-800  sm:text-2xl md:-mb-4 -mb-1">
    Wie lange und wo werden die Daten gespeichert?
</h3>
<p>
    Anfragen für CSS-Assets speichert Google einen Tag lang auf Ihren Servern, die hauptsächlich außerhalb der EU angesiedelt sind. Das ermöglicht uns, mithilfe eines Google-Stylesheets die Schriftarten zu nutzen. Ein Stylesheet ist eine Formatvorlage, über die man einfach und schnell z.B. das Design bzw. die Schriftart einer Webseite ändern kann.
</p>
<p>
    Die Font-Dateien werden bei Google ein Jahr gespeichert. Google verfolgt damit das Ziel, die Ladezeit von Webseiten grundsätzlich zu verbessern. Wenn Millionen von Webseiten auf die gleichen Schriften verweisen, werden sie nach dem ersten Besuch zwischengespeichert und erscheinen sofort auf allen anderen später besuchten Webseiten wieder. Manchmal aktualisiert Google Schriftdateien, um die Dateigröße zu reduzieren, die Abdeckung von Sprache zu erhöhen und das Design zu verbessern.
</p>

<h3 class="text-xl font-extrabold text-gray-800  sm:text-2xl md:-mb-4 -mb-1">
    Wie kann ich meine Daten löschen bzw. die Datenspeicherung verhindern?
</h3>
<p>
    Jene Daten, die Google für einen Tag bzw. ein Jahr speichert können nicht einfach gelöscht werden. Die Daten werden beim Seitenaufruf automatisch an Google übermittelt. Um diese Daten vorzeitig löschen zu können, müssen Sie den Google-Support auf <a href="https://support.google.com/?hl=de&tid=311166606" target="_blank">https://support.google.com/?hl=de&tid=311166606</a> kontaktieren. Datenspeicherung verhindern Sie in diesem Fall nur, wenn Sie unsere Seite nicht besuchen.
</p>
<p>
    Anders als andere Web-Schriften erlaubt uns Google uneingeschränkten Zugriff auf alle Schriftarten. Wir können also unlimitiert auf ein Meer an Schriftarten zugreifen und so das Optimum für unsere Webseite rausholen. Mehr zu Google Fonts und weiteren Fragen finden Sie auf <a href="https://developers.google.com/fonts/faq?tid=311166606" target="_blank">https://developers.google.com/fonts/faq?tid=311166606</a>. Dort geht zwar Google auf datenschutzrelevante Angelegenheiten ein, doch wirklich detaillierte Informationen über Datenspeicherung sind nicht enthalten. Es ist relativ schwierig (beinahe unmöglich), von Google wirklich präzise Informationen über gespeicherten Daten zu bekommen.
</p>
<p>
    Welche Daten grundsätzlich von Google erfasst werden und wofür diese Daten verwendet werden, können Sie auch auf <a href="https://www.google.com/intl/de/policies/privacy/" target="_blank">https://www.google.com/intl/de/policies/privacy/</a> nachlesen.
</p>