<h3 class="text-3xl font-extrabold text-gray-800 sm:text-4xl md:-mb-4 -mb-1">
    TLS-Verschlüsselung mit https
</h3>
<p>
    Wir verwenden https um Daten abhörsicher im Internet zu übertragen
    (Datenschutz durch Technikgestaltung <a href="https://eur-lex.europa.eu/legal-content/DE/TXT/HTML/?uri=CELEX:32016R0679&from=DE&tid=311166606" target="_blank">Artikel 25 Absatz 1 DSGVO</a>).
    Durch den Einsatz von TLS (Transport Layer Security),einem Verschlüsselungsprotokoll
    zur sicheren Datenübertragung im Internet können wir den Schutz vertraulicher Daten sicherstellen.
    Sie erkennen die Benutzung dieser Absicherung der Datenübertragung am kleinen Schloßsymbol links
    oben im Browser und der Verwendung des Schemas https (anstatt http) als Teil unserer Internetadresse.
</p>