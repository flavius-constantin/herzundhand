        Ein Browser sollte folgende Mindestgrößen unterstützen:
    </span>
</p>

    <ul class="md:ml-12 -mt-4 list-none md:list-disc">
        <li>Ein Cookie soll mindestens 4096 Bytes enthalten können</li>
        <li>Pro Domain sollen mindestens 50 Cookies gespeichert werden können</li>
        <li>Insgesamt sollen mindestens 3000 Cookies gespeichert werden könne</li>
    </ul>

<h3 class="text-xl font-extrabold text-gray-800  sm:text-2xl md:-mb-4 -mb-1">
    Welche Arten von Cookies gibt es?
</h3>
<p>
    Die Frage welche Cookies wir im Speziellen verwenden, hängt von den verwendeten Diensten ab und wird in der folgenden Abschnitten der Datenschutzerklärung geklärt. An dieser Stelle möchten wir kurz auf die verschiedenen Arten von HTTP-Cookies eingehen.
</p>
<p>
    Man kann 5 Arten von Cookies unterscheiden:
</p>
<ol class="md:ml-12 list-decimal">
    <li>
        <span class="font-bold">Unbedingt notwendige Cookies</span><br>
        Diese Cookies sind nötig, um grundlegende Funktionen der Website sicherzustellen.
        Zum Beispiel braucht es diese Cookies, wenn ein User ein Produkt in den Warenkorb legt,
        dann auf anderen Seiten weitersurft und später erst zur Kasse geht.
        Durch diese Cookies wird der Warenkorb nicht gelöscht, selbst wenn der User sein Browserfenster schließt.
    </li>
    <li class="mt-4">
        <span class="font-bold">Unbedingt notwendige Cookies</span><br>
        Diese Cookies sind nötig, um grundlegende Funktionen der Website sicherzustellen.
        Zum Beispiel braucht es diese Cookies, wenn ein User ein Produkt in den Warenkorb legt,
        dann auf anderen Seiten weitersurft und später erst zur Kasse geht.
        Durch diese Cookies wird der Warenkorb nicht gelöscht, selbst wenn der User sein Browserfenster schließt.
    </li>
    <li class="mt-4">
        <span class="font-bold">Funktionelle Cookies</span><br>
        Diese Cookies sammeln Infos über das Userverhalten und ob der User etwaige Fehlermeldungen bekommt.
        Zudem werden mithilfe dieser Cookies auch die Ladezeit und das Verhalten der Website bei verschiedenen Browsern gemessen.
    </li>
    <li class="mt-4">
        <span class="font-bold">Zielorientierte Cookies</span><br>
        Diese Cookies sorgen für eine bessere Nutzerfreundlichkeit.
        Beispielsweise werden eingegebene Standorte,
        Schriftgrößen oder Formulardaten gespeichert.
    </li>
    <li class="mt-4">
        <span class="font-bold">Werbe-Cookies</span><br>
        Diese Cookies werden auch Targeting-Cookies genannt.
        Sie dienen dazu dem User individuell angepasste Werbung zu liefern.
        Das kann sehr praktisch, aber auch sehr nervig sein.
    </li>
</ol>
<p>
    Üblicherweise werden Sie beim erstmaligen Besuch einer Webseite gefragt,
    welche dieser Cookiearten Sie zulassen möchten. Und natürlich wird diese Entscheidung auch in einem Cookie gespeichert.
</p>


<h3 class="text-xl font-extrabold text-gray-800  sm:text-2xl md:-mb-4 -mb-1">
    Wie kann ich Cookies löschen?
</h3>
<p>
    Wie und ob Sie Cookies verwenden wollen, entscheiden Sie selbst.
    Unabhängig von welchem Service oder welcher Website die Cookies stammen,
    haben Sie immer die Möglichkeit Cookies zu löschen, nur teilweise zuzulassen
    oder zu deaktivieren. Zum Beispiel können Sie Cookies von Drittanbietern blockieren,
    aber alle anderen Cookies zulassen.
</p>
<p>
    Wenn Sie feststellen möchten, welche Cookies in Ihrem Browser gespeichert wurden,
    wenn Sie Cookie-Einstellungen ändern oder löschen wollen,
    können Sie dies in Ihren Browser-Einstellungen finden:
</p>
