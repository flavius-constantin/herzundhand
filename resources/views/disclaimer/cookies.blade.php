    z.B. Edge, Firefox, Safari aus.
</p>
<h3 class="text-xl font-extrabold text-gray-800  sm:text-2xl md:-mb-4 -mb-1">
    Wie sieht es mit meinem Datenschutz aus?
</h3>
<p>
    Seit 2009 gibt es die sogenannten „Cookie-Richtlinien“. Darin ist festgehalten,
    dass das Speichern von Cookies eine Einwilligung des Website-Besuchers (also von Ihnen) verlangt.
    Innerhalb der EU-Länder gibt es allerdings noch sehr unterschiedliche Reaktionen auf diese Richtlinien.
    In Deutschland wurden die Cookie-Richtlinien nicht als nationales Recht umgesetzt.
    Stattdessen erfolgte die Umsetzung dieser Richtlinie weitgehend in § 15 Abs.3 des Telemediengesetzes (TMG).
</p>
<p>
    Wenn Sie mehr über Cookies wissen möchten und vor technischen Dokumentationen nicht zurückscheuen,
    empfehlen wir <a href="https://tools.ietf.org/html/rfc6265" target="_blank">https://tools.ietf.org/html/rfc6265</a>, dem Request for Comments der
    Internet Engineering Task Force (IETF) namens “HTTP State Management Mechanism”.
</p>