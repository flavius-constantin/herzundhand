    So können Sie bei jedem einzelnen Cookie entscheiden, ob Sie das Cookie erlauben oder nicht.
    Die Vorgangsweise ist je nach Browser verschieden. Am besten ist es Sie suchen die Anleitung
    in Google mit dem Suchbegriff “Cookies löschen Chrome” oder “Cookies deaktivieren Chrome” im
